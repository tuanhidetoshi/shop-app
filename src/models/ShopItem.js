class ShopItem {
  constructor(id, name, image, price, category, content, quantity, addedDate) {
    this.id = id;
    this.name = name;
    this.image = image;
    this.price = price;
    this.category = category;
    this.content = content;
    this.quantity = quantity;
    this.addedDate = addedDate;
  }
}

export default ShopItem;
